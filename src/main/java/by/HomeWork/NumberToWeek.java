package by.HomeWork;


public class NumberToWeek {

    /**
     * Возвращает правильную форму слова "неделя" в зависимости от количества недель,
     * вычисляемого из переданного количества дней. Форма слова выбирается по правилам
     * русского языка с учётом склонения числительных.

     * @param day количество дней для пересчёта в недели
     * @return строка с правильной формой слова: "неделя", "недели" или "недель"
     *
     */
    public static String toWeek(int day) {
        int weeks = day / 7;
        if (weeks % 100 >= 11 && weeks % 100 <= 14) {
            return "недель";
        }
        return switch (weeks % 10) {
            case 1 -> "неделя";
            case 2, 3, 4 -> "недели";
            default -> "недель";
        };
    }
}

